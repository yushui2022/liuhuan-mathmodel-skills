def load_questions(model_route: dict[str, Any] | None) -> list[dict[str, Any]]:
    raw_questions = model_route.get("questions") if isinstance(model_route, dict) else None
    if not isinstance(raw_questions, list) or not raw_questions:
        return default_questions()

    questions: list[dict[str, Any]] = []
    for index, item in enumerate(raw_questions, start=1):
        if not isinstance(item, dict):
            continue
        question = dict(item)
        question["question_id"] = question_id(question, index)
        question.setdefault("title", f"问题{index}")
        question.setdefault("task_type", "综合建模/统计分析")
        question.setdefault("baseline_model", "")
        question.setdefault("main_model", question.get("baseline_model", ""))
