def preserve_existing_by_question(
    existing_items: list[dict[str, Any]],
    draft_items: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    existing_by_qid = {str(item.get("question_id")): item for item in existing_items if isinstance(item, dict)}
    merged: list[dict[str, Any]] = []
    used: set[str] = set()
    for draft in draft_items:
        qid = str(draft.get("question_id"))
        existing = existing_by_qid.get(qid)
        if existing and status_of(existing) not in DRAFT_STATUSES:
            merged.append(existing)
        else:
            merged.append(draft)
        used.add(qid)
    for qid, existing in existing_by_qid.items():
        if qid not in used:
            merged.append(existing)
    return merged


def preserve_existing_tables(
    existing_tables: list[dict[str, Any]],
    draft_tables: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    draft_by_id = {str(item.get("table_id")): item for item in draft_tables if isinstance(item, dict)}
